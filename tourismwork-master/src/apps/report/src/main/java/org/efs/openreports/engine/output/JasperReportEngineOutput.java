package org.efs.openreports.engine.output;

import java.io.Serializable;
import java.util.Map;

/**
 * JasperReportEngineOutput extends ReportEngineOuput to provides access to
 * additional information generated by JasperReportEngine.
 * 
 * @author Erik Swenson
 */

public class JasperReportEngineOutput extends ReportEngineOutput implements Serializable
{		
	private static final long serialVersionUID = 8576675614656607178L;	
	
	private Map imagesMap;

	/*
	 * ImagesMap is used to store images generated by the JasperReports HTML
	 * export process.
	 */
	public Map getImagesMap()
	{
		return imagesMap;
	}

	public void setImagesMap(Map imagesMap)
	{
		this.imagesMap = imagesMap;
	}	
}