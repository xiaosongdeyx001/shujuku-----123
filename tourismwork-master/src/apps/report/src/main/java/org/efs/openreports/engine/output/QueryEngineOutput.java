package org.efs.openreports.engine.output;

import java.io.Serializable;
import java.util.List;

import org.efs.openreports.util.DisplayProperty;

/**
 * QueryEngineOutput extends ReportEngineOuput to provides access to
 * additional information generated by QueryReportEngine.
 * 
 * @author Erik Swenson
 */

public class QueryEngineOutput extends ReportEngineOutput implements Serializable
{	
	private static final long serialVersionUID = 2880837329504860633L;
	
	private List results;
	private DisplayProperty[] properties;	
	
	public DisplayProperty[] getProperties()
	{
		return properties;
	}

	public void setProperties(DisplayProperty[] properties)
	{
		this.properties = properties;
	}

	public List getResults()
	{
		return results;
	}

	public void setResults(List results)
	{
		this.results = results;
	}
}
